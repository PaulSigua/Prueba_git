    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed
        if (txt_Nombre.getText().trim().isEmpty() || txt_Precio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ningun campo puede estar vacío");
        } else {
            Servicio serUpdate = new Servicio();
            serUpdate.setId(servicioSeleccionado.getId());
            serUpdate.setNombre(txt_Nombre.getText().trim());
            serUpdate.setPrecio(Double.parseDouble(txt_Precio.getText().trim()));

            if (combo_IVA.getSelectedIndex() == 0) {
                serUpdate.setIva('S');
            } else {
                serUpdate.setIva('N');
            }
            if (combo_Estado.getSelectedIndex() == 0) {
                serUpdate.setEstado('S');
            } else {
                serUpdate.setEstado('N');
            }
            this.controladorServicio.actualizarServicio(serUpdate);
        }
        actulizarTabla();
        resetear();
    }//GEN-LAST:event_btn_ModificarActionPerformed

