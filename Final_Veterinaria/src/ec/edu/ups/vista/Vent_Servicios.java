    private void btn_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegistrarActionPerformed
        if (txt_Nombre.getText().trim().isEmpty() || txt_Precio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ningun campo puede estar vacío");
        } else {
            char iva;
            if (combo_IVA.getSelectedIndex() == 0) {
                iva = 'S';
            } else {
                iva = 'N';;
            }
            Double precio=null;
            Boolean flag=false;
            try {
                precio=Double.parseDouble(txt_Precio.getText().trim());
                flag=true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
            }
            
            if (flag) {
                this.controladorServicio.registrarServicio(new Servicio(0, precio, txt_Nombre.getText().trim(), iva, 'S'));
            }

            
        }
        resetear();
        actulizarTabla();
    }//GEN-LAST:event_btn_RegistrarActionPerformed

    public void resetear() {
        txt_Nombre.setText("");
        txt_Precio.setText("");
        tabla_Servicios.setEnabled(true);
        btn_Registrar.setEnabled(true);
        btn_Buscar.setEnabled(true);
        btn_Modificar.setEnabled(false);
        btn_Eliminar.setEnabled(false);
        combo_Estado.setEnabled(false);
        combo_Estado.setSelectedItem("Activo");
    }

