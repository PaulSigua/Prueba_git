    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        if (tipoMascotaSeleccionada != null) {
            this.vent_Mascotas.setTipoMascotaSeleccionada(tipoMascotaSeleccionada);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "SElECCIONE UN TIPO DE MASCOTA");
        }
    }//GEN-LAST:event_btn_AceptarActionPerformed

    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        if (tipoMascotaSeleccionada != null) {
            this.controladorTipoMascota.eliminarTipoMascota(tipoMascotaSeleccionada);
            actulizarTabla();
            btn_Eliminar.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null, "SElECCIONE UN TIPO DE MASCOTA");
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

