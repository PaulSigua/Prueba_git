    public Seleccionar_Tipo_Mascota() {
        initComponents();
        this.setLocationRelativeTo(null);

        tabla_Tipos.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent event) {
                // obtener la fila seleccionada
                if (!procesandoEvento) {
                    procesandoEvento = true;
                    int filaSeleccionada = tabla_Tipos.getSelectedRow();
                    System.out.println("Fila seleccionada: " + filaSeleccionada);
                    if (filaSeleccionada != -1) {
                        System.out.println("Tipo seleccionado: " + listaActual.get(filaSeleccionada).toString());
                        tipoMascotaSeleccionada = listaActual.get(filaSeleccionada);

                    }
                    btn_Eliminar.setEnabled(true);
                    procesandoEvento = false;
                }
            }
        });
    }

