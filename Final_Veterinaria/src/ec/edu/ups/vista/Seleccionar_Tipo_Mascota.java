    public void actulizarTabla() {
        listaTipoMascotas = this.controladorTipoMascota.listarTipoMascotas();
        listaActual = listaTipoMascotas;
        DefaultTableModel modelo = (DefaultTableModel) tabla_Tipos.getModel();

        modelo.setRowCount(0);

        for (Tipo_Mascota tip : listaTipoMascotas) {
            System.out.println(tip.toString());
            Object[] fila = {tip.getId(), tip.getNombre()};
            modelo.addRow(fila);
        }

        tabla_Tipos.setModel(modelo);

    }

    public void resultadoBusqueda(List<Tipo_Mascota> tiposMascota) {
        DefaultTableModel modelo = (DefaultTableModel) tabla_Tipos.getModel();

        modelo.setRowCount(0);

        for (Tipo_Mascota tip : tiposMascota) {
            System.out.println(tip.toString());
            Object[] fila = {tip.getId(), tip.getNombre()};
            modelo.addRow(fila);
        }
        listaActual = tiposMascota;

        tabla_Tipos.setModel(modelo);
    }

