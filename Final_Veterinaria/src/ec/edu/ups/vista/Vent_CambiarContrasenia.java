    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txt_usuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El usuario no puede estar vacío");
        } else if (txt_ContrasenaAct.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "La contraseña no puede estar vacía");

        } else if (txt_ContrasenaNueva.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "La contraseña nueva no puede estar vacía");

        } else if (txt_ContrasenaNuevaConf.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "La confirmacion contraseña no puede estar vacía");

        } else {
            String passAct = new String(txt_ContrasenaAct.getPassword());
            String passNueva = new String(txt_ContrasenaNueva.getPassword());
            String passNuevaConf = new String(txt_ContrasenaNuevaConf.getPassword());

            if (usuarioIngresado.getClave().equals(passAct)) {
                if (passNueva.equals(passNuevaConf)) {

                    Boolean flag = this.controladorUsuario.cambiarContrasenia(usuarioIngresado, passNueva);
                    if (flag) {
                        this.dispose();
                        this.menu_Principal.descativar();
                        this.controladorUsuario.setUsuarioIngresado(null);
                        Vent_Inicio ventInicio = new Vent_Inicio(this.controladorUsuario, this.menu_Principal);
                        Menu_Principal.instance.jDesktopPane1.add(ventInicio);
                        ventInicio.setVisible(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "La contraseña nueva no coincide con su confrimacion", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Contraseña Acutal Incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


