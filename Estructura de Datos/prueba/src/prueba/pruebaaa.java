    private void persona1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_persona1KeyPressed
        
        int der = persona1.getX();
        int izq = persona1.getY();
        
        if(evt.getKeyChar() == 'n'){
            der += 5;
            persona1.setLocation(der, izq);
        }
        if(evt.getKeyChar() == 'l'){
            izq -= 5;
            persona1.setLocation(der, izq);
        }
    }//GEN-LAST:event_persona1KeyPressed

