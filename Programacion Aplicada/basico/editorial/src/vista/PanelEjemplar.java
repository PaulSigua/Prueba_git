    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int codEjemplar = Integer.valueOf(txtID.getText());
        
        int anio=txtFecha.getCalendar().get(Calendar.YEAR);
        int mes=txtFecha.getCalendar().get(Calendar.MONTH);
        int dia=txtFecha.getCalendar().get(Calendar.DAY_OF_MONTH);
        String fecha=anio+"/"+mes+"/"+dia;
        
        int numPag=Integer.valueOf(txtNumPaginas.getText());
        int numVentas=Integer.valueOf(txtNumVentas.getText());
        int codRev=Integer.valueOf(txtRevista.getText());
        
        boolean codREevista=controladorEjemplar.agregarEjemplar(codEjemplar,fecha,numPag,numVentas, codRev);
    }//GEN-LAST:event_btnAgregarActionPerformed

