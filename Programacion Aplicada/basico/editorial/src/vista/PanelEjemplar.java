    private void btnBuscarRevistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRevistaActionPerformed
        ResultSet listado = controladorEjemplar.buscarRevista(Integer.valueOf(txtRevista.getText()));

        boolean existeRevista =  false;
        try {
            while (listado.next()) {
               lblRevista.setText(listado.getString("titulo"));
               existeRevista=true;
            }if(existeRevista==false){
                lblRevista.setText("No existe la revista");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelEjemplar.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_btnBuscarRevistaActionPerformed

