    public int tiempo() {
        int m = 0, s = 0;
        s++;
        if (s == 60) {
            s = 0;
            ++s;
        }
        if (m == 60) {
            m = 0;
            ++m;
        }
        actualizarLabel();
        return m + s;
    }

