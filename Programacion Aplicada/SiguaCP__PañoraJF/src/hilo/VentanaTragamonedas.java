    public void iniciarHilo(ActionEvent evt) {
        try {
            if (evt.getSource().equals(iniciar1Button)) {
                if (llaveh1 == false) {
                    this.tiempo = Integer.valueOf(jTextField2.getText());
                    if (tiempo == 0) {
                        JOptionPane.showMessageDialog(this, "El tiempo no puede ser 0");
                    } else {
                        h1 = new Hilo(this.tiempo, this, evt, 1);
                        //System.out.println("Estado del Hilo 1" + h1.isAlive());
                        llaveh1 = true;
                        h1.start();
                    }
                }
            } else if (evt.getSource().equals(iniciar2Button)) {
                if (llaveh2 == false) {
                    this.tiempo = Integer.valueOf(jTextField3.getText());
                    if (tiempo == 0) {
                        JOptionPane.showMessageDialog(this, "El tiempo no puede ser 0");
                    } else {
                        h2 = new Hilo(this.tiempo, this, evt, 2);
                        llaveh2 = true;
                        h2.start();
                    }
                }
            } else if (evt.getSource().equals(iniciar3Button)) {
                if (llaveh3 == false) {
                    this.tiempo = Integer.valueOf(jTextField5.getText());
                    if (tiempo == 0) {
                        JOptionPane.showMessageDialog(this, "El tiempo no puede ser 0");
                    } else {
                        h3 = new Hilo(this.tiempo, this, evt, 3);
                        llaveh3 = true;
                        h3.start();
                    }
                }
            } else if (evt.getSource().equals(iniciarTodosButton)) {
                if (llaveh1 == false && llaveh2 == false && llaveh3 == false) {
                    this.tiempo = Integer.valueOf(jTextField1.getText());
                    if (tiempo == 0) {
                        JOptionPane.showMessageDialog(this, "Debe ingresar un valor diferente de 0");
                    } else {
                        jTextField2.setText(tiempo + "");
                        jTextField3.setText(tiempo + "");
                        jTextField5.setText(tiempo + "");
                        h1 = new Hilo(tiempo, this, evt, 1);
                        h2 = new Hilo(tiempo, this, evt, 2);
                        h3 = new Hilo(tiempo, this, evt, 3);
                        llaveh1 = true;
                        llaveh2 = true;
                        llaveh3 = true;
                        h1.start();
                        h2.start();
                        h3.start();
                    }
                }

            }
        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el tiempo :( ");
        }
    }

    public void detenerHilo(ActionEvent evt) {
        try {
            if (evt.getSource().equals(parar1Button)) {
                llaveh1 = false;
                h1.interrupt();
                //System.out.println("Estado del Hilo 1: " + h1.isAlive());
                validarJuego();
            } else if (evt.getSource().equals(parar2Button)) {
                llaveh2 = false;
                h2.interrupt();
                validarJuego();
            } else if (evt.getSource().equals(parar3Button)) {
                llaveh3 = false;
                h3.interrupt();
                validarJuego();
            } else if (evt.getSource().equals(pararTodosButton)) {
                llaveh1 = false;
                llaveh2 = false;
                llaveh3 = false;
                h1.interrupt();
                h2.interrupt();
                h3.interrupt();
                validarJuego();
            }
        } catch (NullPointerException ex) {
            System.out.println("");
        }
    }

    public void validarJuego() {
        try {
            if ((h1 == null || h1.isInterrupted())) {
                if (h2 == null || h2.isInterrupted()) {
                    if (h3 == null || h3.isInterrupted()) {
                        if (jLabel1.getY() == jLabel6.getY() && jLabel6.getY() == jLabel9.getY()) {
                            JOptionPane.showMessageDialog(this, "\n         BIEN JUGADO \n     Â¡HAS GANAGO! :D");
                        } else {
                            JOptionPane.showMessageDialog(this, "      GAME OVER D:  ");
                        }

                    }
                }
            }
        } catch (NullPointerException ex) {
            System.out.println("error");
        }

    }

