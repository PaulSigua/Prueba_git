    private void conectarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectarButtonActionPerformed
        
        if (cont == 0) {
            bateria = Integer.parseInt(ingresarPorcentajeTextField.getText());
            timer = new Timer();
            tarea2 = new Temp2TimerTask(this, sujeto);
            new BateriaObservador(sujeto, this);
            new TiempoEstObservador(sujeto, this);
            sujeto.setEstado(bateria);
            jProgressBar1.setValue(bateria);

            timer.schedule(tarea2, 0, 1000);
            tarea = new TempTimerTask(sujeto, bateria, this);
            timer2 = new Timer();
            timer2.schedule(tarea, 0, 30000);
        } else {
            timer = new Timer();
            tarea2 = new Temp2TimerTask(this, sujeto);
            new BateriaObservador(sujeto, this);
            new TiempoEstObservador(sujeto, this);
            sujeto.setEstado(bateria);
            jProgressBar1.setValue(bateria);

            timer.schedule(tarea2, 0, 1000);
            tarea = new TempTimerTask(sujeto, bateria, this);
            timer2 = new Timer();
            timer2.schedule(tarea, 0, 30000);
        }
        cont++;
    }//GEN-LAST:event_conectarButtonActionPerformed

